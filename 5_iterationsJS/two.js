// while & do while loops
let index = 0
while (index <= 5) {
    console.log(`The value of index is ${index}`);
    index = index + 1
}

let indieSupes = ["Inspector Steel", "Doga", "Shakti", "Anthony"]
let indieNum = 0
while (indieNum < indieSupes.length) {
    console.log(`SuperHero - ${indieSupes[indieNum]}`);
    indieNum = indieNum + 1
}

// do while loop
let score = 0
do {
    console.log(`The score is ${score}`);
    score++
} while (score <= 5);
